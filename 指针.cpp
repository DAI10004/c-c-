#include <stdio.h>

int main() {
    int a[3][4];
    int (*q)[4];
    q = a;// 让 q 指向二维数组 a 的首行，即 a[0] 的地址
    *(*q) = 9;// 对 *(*q) 赋值，这里相当于给 a[0][0] 赋值 
  	q++;// q 向后移动一行，指向 a[1]   
    *(*q) = 8;// 对 a[1][0] 赋值  
    q++;// q 再向后移动一行，指向 a[2]  
    *(*q) = 7;// 对 a[2][0] 赋值
    
    printf("%d\n", a[0][0]);
    printf("%d\n", a[1][0]);
    printf("%d\n", a[2][0]);

    return 0;
}
